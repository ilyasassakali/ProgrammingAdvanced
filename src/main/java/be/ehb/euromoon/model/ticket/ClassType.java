package be.ehb.euromoon.model.ticket;

/**
 * Represents the class types available for train tickets.
 *
 * <p>This enum defines the two available seating classes for train journeys
 * within the Euromoon railway system.
 */
public enum ClassType {
    /**
     * First class seating with premium amenities.
     */
    FIRST,

    /**
     * Second class seating with standard amenities.
     */
    SECOND
}
